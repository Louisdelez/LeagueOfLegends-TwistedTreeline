use serde::{Deserialize, Serialize};

/// Champion class identifier (used for ability kit selection)
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum ChampionClass {
    Mage,
    Fighter,
    Tank,
}

/// Real champion identifier
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize, Hash)]
pub enum ChampionId {
    Annie, Garen, Ashe, Darius, Lux,
    Thresh, Jinx, Yasuo, MasterYi, Jax,
    Teemo, Singed, Tryndamere, Mordekaiser, Poppy,
}

impl ChampionId {
    pub fn all() -> &'static [ChampionId] {
        &[
            ChampionId::Annie, ChampionId::Garen, ChampionId::Ashe,
            ChampionId::Darius, ChampionId::Lux, ChampionId::Thresh,
            ChampionId::Jinx, ChampionId::Yasuo, ChampionId::MasterYi,
            ChampionId::Jax, ChampionId::Teemo, ChampionId::Singed,
            ChampionId::Tryndamere, ChampionId::Mordekaiser, ChampionId::Poppy,
        ]
    }

    pub fn ability_class(&self) -> ChampionClass {
        match self {
            ChampionId::Annie | ChampionId::Lux | ChampionId::Teemo => ChampionClass::Mage,
            ChampionId::Ashe | ChampionId::Jinx => ChampionClass::Mage, // ranged ADC use mage kit
            ChampionId::Garen | ChampionId::Darius | ChampionId::Yasuo
            | ChampionId::MasterYi | ChampionId::Jax | ChampionId::Tryndamere
            | ChampionId::Singed | ChampionId::Mordekaiser => ChampionClass::Fighter,
            ChampionId::Thresh | ChampionId::Poppy => ChampionClass::Tank,
        }
    }
}

/// Base stats for a champion at level 1
#[derive(Debug, Clone)]
pub struct ChampionDef {
    pub class: ChampionClass,
    pub champion_id: Option<ChampionId>,
    pub name: &'static str,
    pub title: &'static str,
    pub hp: f32, pub hp_per_level: f32,
    pub mana: f32, pub mana_per_level: f32,
    pub ad: f32, pub ad_per_level: f32,
    pub ap: f32,
    pub armor: f32, pub armor_per_level: f32,
    pub mr: f32, pub mr_per_level: f32,
    pub attack_speed: f32, pub move_speed: f32, pub attack_range: f32,
    pub q_name: &'static str, pub w_name: &'static str,
    pub e_name: &'static str, pub r_name: &'static str,
    pub passive_name: &'static str,
    pub q_cd: [f32; 5], pub w_cd: [f32; 5], pub e_cd: [f32; 5], pub r_cd: [f32; 3],
    pub q_dmg: [f32; 5], pub w_dmg: [f32; 5], pub e_dmg: [f32; 5], pub r_dmg: [f32; 3],
    pub q_ap_ratio: f32, pub w_ap_ratio: f32, pub e_ad_ratio: f32, pub r_ap_ratio: f32,
    pub color: [f32; 3], pub emissive: [f32; 3],
}

/// Get champion by class (legacy, used by ability system)
pub fn get_champion(class: ChampionClass) -> ChampionDef {
    match class {
        ChampionClass::Mage => get_champion_by_id(ChampionId::Annie),
        ChampionClass::Fighter => get_champion_by_id(ChampionId::Garen),
        ChampionClass::Tank => get_champion_by_id(ChampionId::Poppy),
    }
}

